<?php

namespace Samsin33\DecisionEngine\Models;

use Illuminate\Database\Eloquent\Relations\BelongsTo;
use Illuminate\Database\Eloquent\Model;
use Illuminate\Support\Facades\Auth;
use Illuminate\Support\Facades\Validator;

class RuleExecutionLog extends Model
{
    /**
     *
     * @var array<string>
     */
    protected $fillable = ['rule_execution_id', 'previous_attributes', 'new_attributes'];

    /**
     *
     * @var array<string>
     */
    protected $visible = ['id', 'rule_execution_id', 'previous_attributes', 'new_attributes', 'created_at', 'updated_at'];

    protected static function booted()
    {
        parent::booted(); // TODO: Change the autogenerated stub

        self::creating(function () {
            $this->ipaddress = request()->ip();
        });
    }

    //--------------------- Relationships ------------------------------

    /**
     *
     * @return BelongsTo
     */
    public function ruleExecution(): BelongsTo
    {
        return $this->belongsTo(RuleExecution::class, 'rule_execution_id', 'id');
    }
}
